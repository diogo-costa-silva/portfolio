#!/usr/bin/env node
// Adapter: canonical projects.json -> Markdown tables grouped by category.
// Prints to stdout, or with --write injects between the markers
//   <!-- PORTFOLIO:START --> ... <!-- PORTFOLIO:END -->
// in the given file (e.g. the diogo-costa-silva profile README).
// Usage:
//   node adapters/to-profile-readme.mjs
//   node adapters/to-profile-readme.mjs --write <path-to-README.md>

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const canon = JSON.parse(readFileSync(join(ROOT, "data/projects.json"), "utf8"));
const taxonomy = JSON.parse(readFileSync(join(ROOT, "data/taxonomy.json"), "utf8"));

const link = (p) => {
  const parts = [`[Code](${p.links.repo})`];
  if (p.links.demo) parts.push(`[Demo](${p.links.demo})`);
  return parts.join(" · ");
};

let md = "";
for (const cat of [...taxonomy.categories].sort((a, b) => a.order - b.order)) {
  const items = canon.projects.filter((p) => p.visible && p.category === cat.id);
  if (!items.length) continue;
  md += `### ${cat.label}\n\n`;
  md += `| Project | Description | Tech | Links |\n|---|---|---|---|\n`;
  for (const p of items) {
    md += `| **${p.title}** | ${p.tagline || p.description} | ${p.tags.slice(0, 4).join(", ")} | ${link(p)} |\n`;
  }
  md += `\n`;
}
md = md.trimEnd() + "\n";

const writeIdx = process.argv.indexOf("--write");
if (writeIdx === -1) { process.stdout.write(md); process.exit(0); }

const file = process.argv[writeIdx + 1];
if (!file) { console.error("Usage: --write <README.md>"); process.exit(1); }
const src = readFileSync(file, "utf8");
const START = "<!-- PORTFOLIO:START -->", END = "<!-- PORTFOLIO:END -->";
if (!src.includes(START) || !src.includes(END)) {
  console.error(`Markers ${START} / ${END} not found in ${file}. Add them where the project list should go.`);
  process.exit(1);
}
const out = src.replace(
  new RegExp(`${START}[\\s\\S]*${END}`),
  `${START}\n<!-- generated by portfolio/adapters/to-profile-readme.mjs — do not edit by hand -->\n\n${md}\n${END}`
);
writeFileSync(file, out);
console.log(`✓ Injected project tables into ${file}.`);
