#!/usr/bin/env node
// Adapter: canonical -> the portfolio repo's OWN README showcase (the GitHub-native
// vitrine recruiters reach via the profile's "All projects" link + pin).
// Injects a Featured block + all projects grouped by theme, between the markers.
// Usage: node tools/adapter-portfolio-readme.mjs   (writes README.md in the repo root)

import { join } from "node:path";
import { loadCanon, loadTaxonomy, loadRoadmap, renderByTheme, renderFeatured, renderRoadmap, injectMarkers, ROOT } from "./lib.mjs";

const canon = loadCanon();
const taxonomy = loadTaxonomy();
const visible = canon.projects.filter((p) => p.visible);
const featured = visible.filter((p) => p.featured).sort((a, b) => b.weight - a.weight);
const roadmap = loadRoadmap();

let md = "";
if (featured.length) md += `## ⭐ Featured\n\n${renderFeatured(featured)}\n`;
md += `## 📂 All Projects\n\n${renderByTheme(visible, taxonomy)}`;
if (roadmap.length) md += `\n## 🔭 Roadmap — What's next\n\n_Ideas I plan to build. Not started yet, so there's no code to show — just the problems I want to solve._\n\n${renderRoadmap(roadmap, taxonomy)}`;

injectMarkers(join(ROOT, "README.md"), md, "generated by portfolio/tools/adapter-portfolio-readme.mjs — do not edit by hand");
console.log(`✓ Rebuilt README.md showcase — ${featured.length} featured, ${visible.length} total, ${roadmap.length} roadmap.`);
